import math

x = float(input("Enter any number: "))

print("The square root of", x, "is", math.sqrt(x))
print("e to the power of", x, "is", math.exp(x))
print("The lowest integer value of", x, "is", math.floor(x))
print("The highest integer value of", x, "is", math.ceil(x))
print("The factorial of", int(x), "is", math.factorial(int(x)))