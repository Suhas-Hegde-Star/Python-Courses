import math

sinsin = (math.sin(int(input("Enter a number to find its sine: "))))
coscos = (math.cos(int(input("Enter a number to find its cosine: "))))
tantan = (math.tan(int(input("Enter a number to find its tangent: "))))

print("Sine value is:", sinsin)
print("Cosine value is:", coscos)
print("Tangent value is:", tantan)