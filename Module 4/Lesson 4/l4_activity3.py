# Set the Set value of the Set
set1 = {"Hello", "World","Can", "Be", "Written", "Python", "is", "my"}
set2 = {"Python", "is", "my", "Java", "C++", "World"}

# Print the Set value of the Set
print("Set 1:", set1)
print("Set 2:", set2)

# Intersection Of Sets
set3 = set1.intersection(set2)
